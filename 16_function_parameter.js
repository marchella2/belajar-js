// Contoh function dengan dua parameter
function cetakData(nama, umur){
    console.log('Nama : ' + nama);
    console.log('Umur : ' + umur);
    console.log('========');
}

cetakData('Amir', 20)
cetakData('Budi', 21)
cetakData('Charlie', 22)

// Contoh function dengan 3 parameter
function cetakDataLagi(kelurahan, kecamatan, kota)
{
    console.log('Kelurahan : ' + kelurahan);
    console.log('Kecamatan : ' + kecamatan);
    console.log('Kota : ' + kota);
    console.log('========');
}

cetakDataLagi('Rangkapan Jaya', 'Pancoran Mas', 'Depok')